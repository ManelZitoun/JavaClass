package com.class2;

public class StringPractice {
		public 	String firstName="John";
			public	String lastName ="Smith";
	
	public static void main(String [] args) {
		
		//My name is John and my last name is Smith
		
		String firstName="John";
		String lastName ="Smith";
		
		
		System.out.println("My name is "+firstName+" and my last name is "+lastName+".");
		
		//I am a B student 
		
		char grade='B';
		//We could use string, but get in the habit of using char for practice. 
		
		System.out.println("I am a "+grade+" student.");
		
		//I live in the city of Fairfax in the state of Virginia
		
		String city="Fairfax";
		String state="VA";
		
		System.out.println("I live in the city of "+city+" in the state of "+state);
		
		//My phone number is 5406914224
		
		long phone=5406914224l;
		
		System.out.println("My phone number is "+phone);
		
		
		
		
		
		
		
	}

}

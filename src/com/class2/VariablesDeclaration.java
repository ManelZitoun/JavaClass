package com.class2;

public class VariablesDeclaration {

	public static void main(String[] args) {

		// different ways to create/declare variables
		// 1. create variable and assign value
		int num = 12;

		// 2 ways: using 2 steps
		int y; // 1. declaring variable
		y = 12; // 2. assigning the value

		// Declaring the variables one by one
		int a;
		int b;
		int c;
		int d;
		// OR declaring them all together
		int x, q, z, m;

		a = 30;
		b = 50;
		c = 70;
		d = 20;

		x = 23;
		q = 19;
		z = 30;
		m = 45;
		
		String name="John";
		System.out.println(name); 
		
		//for mac use cmd+shift+f = formating (organizes code)
		

	}

}

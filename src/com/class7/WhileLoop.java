package com.class7;

public class WhileLoop {
public static void main(String[] args) {
	
	int a=1;
	
	while (a<10) {
		System.out.println("Good morning");
		a++;
	}
	int b=0;
	
	while (b<=5) {
		System.out.println("I am inside the while loop"+b);
		b++;
	}
	System.out.println("I am outside of the while loop"); //once the statement is false it will print out this statement}

//I want to print values from 10 to 20 inclusive 
	
	int z=10;
	
	while (z<=20) {
		System.out.println(z);
		z++;
	} System.out.println("I'm done");

int q=1;

while (q<=20) {
	System.out.println(q);
	q++;
}
System.out.println("leave loop");

int e=10;

while(e>=1) {
	System.out.println(e);
	e--;
}
	

}
}
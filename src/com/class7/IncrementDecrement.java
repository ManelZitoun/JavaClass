public class IncrementDecrement {
	public static void main(String[] args) {
		
		int y=3;
		y+=1; //4
		
		int z=5;
		z-=1; //4
		
		int a=10;
		int b=++a; //11
		
		int c=10;
		int d=c++; //int d=10 & int c=11
		
		int q=100;
		int e=q--; // int e & q are 99
		
		int h=50;
		int g=--h; // value on h & g = 49 
		
		
		
		
		
	}

}
